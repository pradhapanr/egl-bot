class PlayerStats {
  constructor(name, champion, kills, deaths, assists, minions, gold, damage) {
    this.name = name;
    this.champion = champion;
    this.kills = kills;
    this.deaths = deaths;
    this.assists = assists;
    this.minions = minions;
    this.gold = gold;
    this.damage = damage;
  }
}

export default PlayerStats;
